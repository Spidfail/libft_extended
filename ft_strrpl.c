#include "libft.h"

char	*ft_strrpl(char **s1, char **s2)
{
	char	*tmp;

	tmp = ft_strjoin(*s1, *s2);
	free(*s1);
	free(*s2);
	*s1 = NULL;
	*s2 = NULL;
	return (tmp);
}
